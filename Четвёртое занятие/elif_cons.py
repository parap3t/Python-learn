# Условная конструкция elif
name = input("Введите своё имя: ")

if name == "Alexandr":
    print("Вы Александр!")
  
elif name == "Danil":
    print("Вы Данил!")
    
    
number = int(input("Введите число :"))

if number > 0:
    print("Число положительное!")

elif number < 0:
    print("Число отрицательное!")
    
    
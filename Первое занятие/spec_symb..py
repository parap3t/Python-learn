# Специальные символы

print("Hello, \nworld") # -> Hello,
                        # -> world
                        
print("\nHello, world") # -> 
                        # -> Hello, world!                      
                        
print("Hello, \tworld") # -> Hello,     world
print("Hello, \t\tworld") # -> Hello,         world

print("Hello 'world' ") # -> Hello 'world'
print("Hello, \"world\" ") # ->  Hello "world"

print("\"Hello\", \nwor\tld") # -> "Hello",
                              # -> wor    ld
                          
        
# Параметры функции

print("Hello, world") # -> Hello, world

print("Hello, world", end="\n") # -> Hello, world

print("Hello, world", end=" ") # -> Hello, world Whatsup
print("Whatsup")

print("Hello", "world", sep="-") # -> Hello world
print("Hello", "world", sep="\t") # -> Hello    world

print("Hello, world", sep="-", end=" ") # -> Hello-world Hello man
print("Hello, man")

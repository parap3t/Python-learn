# Условные выражения 

num1 = 5 > 2 # True
print(num1) # -> True

num1 = 5 < 2 # False
print(num1) # -> False

num1 = 5 <= 2 # False
print(num1) # -> False

num1 = 5 >= 2 # True
print(num1) # -> True

num1 = 5 == 2 # False
print(num1) # -> False

num1 = 5 == 2 and 5 > 2 # False
print(num1) # -> False

num1 = 5 == 2 or 5 > 2 # True
print(num1) # -> True